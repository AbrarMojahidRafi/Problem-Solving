class Solution:
  def climbStairs(self, n):
    count = 0
    
    temp = n
    while (temp > 0):
      if temp % 2 == 0:
        count += temp 
      else: 
        count += 2*temp 
      temp -= 1
    
    return count 
  

s = Solution()
print(s.climbStairs(5))


